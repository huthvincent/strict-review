import time
import statistics
import pytest

REPEAT_N = 30
WARMUP = 5
SEED = 20260720


def _time_median(fn, *args, n=REPEAT_N, warmup=WARMUP, **kwargs):
    import random
    random.seed(SEED)
    for _ in range(warmup):
        fn(*args, **kwargs)
    ts = []
    for _ in range(n):
        t0 = time.perf_counter()
        fn(*args, **kwargs)
        ts.append(time.perf_counter() - t0)
    return statistics.median(ts)


