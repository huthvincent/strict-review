def classify_leaf(leaf, category, page, recipe_map):
    """Return classification dict for one leaf."""
    ap = page.get("antipatterns", []) if page else []
    mech = "; ".join(ap[:3]) if ap else f"(leaf {leaf}, no distilled antipatterns)"
    prior = recipe_map.get(leaf, {})
    u = (f"叶: {leaf}（大类 {category}）\n反模式/机制: {mech[:600]}\n"
         f"已有 leg3 路由建议: recipe={prior.get('recipe')} hint={prior.get('config_hint')}")
    with _cl().messages.stream(model=OPUS, max_tokens=500, thinking={"type": "disabled"},
                               system=SYSTEM, messages=[{"role": "user", "content": u}],
                               tools=[CLASSIFY_TOOL],
                               tool_choice={"type": "tool", "name": "emit_verification"}) as st:
        r = st.get_final_message()
    inp = next((b.input for b in r.content if b.type == "tool_use"), None) or {}
    cost = (r.usage.input_tokens * PRICE_IN + r.usage.output_tokens * PRICE_OUT) / 1e6
    # rule check: route-recipe must have a recipe; microbench must have a target
    vk = inp.get("verify_kind")
    if vk == "route-recipe" and not (inp.get("recipe") or prior.get("recipe")):
        inp["recipe"] = prior.get("recipe") or "gpt-perf"
    entry = {"leaf": leaf, "category": category, "verify_kind": vk, "reason": inp.get("reason", ""),
             "recipe": inp.get("recipe") or prior.get("recipe"), "config_hint": inp.get("config_hint") or prior.get("config_hint"),
             "observed_metric": inp.get("observed_metric"),
             "microbench_target": inp.get("microbench_target"),
             "microbench_input_desc": inp.get("microbench_input_desc"),
             "provenance": {"model": OPUS, "prompt": "leaf_verification.v1", "ts": now_iso(), "cost": round(cost, 5)}}
    return entry


def write_template(entry):
    TDIR.mkdir(parents=True, exist_ok=True)
    leaf = entry["leaf"]
    code = MICROBENCH_TEMPLATE.format(
        leaf=leaf, target=entry.get("microbench_target") or "TODO",
        input_desc=entry.get("microbench_input_desc") or "TODO",
        metric=entry.get("observed_metric") or "latency", smoke_status="untested",
        model=OPUS, ts=now_iso())
    (TDIR / f"{leaf}.py").write_text(code)


